package kimJeongTae;

public class ReservationBean {
	private int num;
	private int user;
	private String name;
	private String phonenum;
	private String email;
	private int population;
	private boolean ccflag;
	private int frnum;
	private String reservdate;
	private boolean carflag;
	private boolean userflag;
	
	
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getUser() {
		return user;
	}
	public void setUser(int user) {
		this.user = user;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhonenum() {
		return phonenum;
	}
	public void setPhonenum(String phonenum) {
		this.phonenum = phonenum;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getPopulation() {
		return population;
	}
	public void setPopulation(int population) {
		this.population = population;
	}
	public boolean isCcflag() {
		return ccflag;
	}
	public void setCcflag(boolean ccflag) {
		this.ccflag = ccflag;
	}
	public int getFrnum() {
		return frnum;
	}
	public void setFrnum(int frnum) {
		this.frnum = frnum;
	}
	public String getReservdate() {
		return reservdate;
	}
	public void setReservdate(String reservdate) {
		this.reservdate = reservdate;
	}
	public boolean getCarflag() {
		return carflag;
	}
	public void setCarflag(boolean carflag) {
		this.carflag = carflag;
	}
	public boolean getUserflag() {
		return userflag;
	}
	public void setUserflag(boolean userflag) {
		this.userflag = userflag;
	}
	
}
