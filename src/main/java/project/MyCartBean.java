package project;

public class MyCartBean {
	  private int num;
	  private int user;
	  private int item;
	  private int amount;
	  private boolean buyflag;
	  
	  
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getUser() {
		return user;
	}
	public void setUser(int user) {
		this.user = user;
	}
	public int getItem() {
		return item;
	}
	public void setItem(int item) {
		this.item = item;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public boolean isBuyflag() {
		return buyflag;
	}
	public void setBuyflag(boolean buyflag) {
		this.buyflag = buyflag;
	}
	  
	  
}
