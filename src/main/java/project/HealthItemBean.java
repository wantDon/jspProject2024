package project;

public class HealthItemBean {
	  private int num;
	  private String name;
	  private String hcontent; 
	  private String fcperiod; 
	  private int ptnum; 
	  private int gxnum; 
	  private int frnum;
	  private int price;
	  private String sellperiod;
	  private boolean sellflag;
	  
	  
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getHcontent() {
		return hcontent;
	}
	public void setHcontent(String hcontent) {
		this.hcontent = hcontent;
	}
	public String getFcperiod() {
		return fcperiod;
	}
	public void setFcperiod(String fcperiod) {
		this.fcperiod = fcperiod;
	}
	public int getPtnum() {
		return ptnum;
	}
	public void setPtnum(int ptnum) {
		this.ptnum = ptnum;
	}
	public int getGxnum() {
		return gxnum;
	}
	public void setGxnum(int gxnum) {
		this.gxnum = gxnum;
	}
	public int getFrnum() {
		return frnum;
	}
	public void setFrnum(int frnum) {
		this.frnum = frnum;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getSellperiod() {
		return sellperiod;
	}
	public void setSellperiod(String sellperiod) {
		this.sellperiod = sellperiod;
	}
	public boolean getSellflag() {
		return sellflag;
	}
	public void setSellflag(boolean sellflag) {
		this.sellflag = sellflag;
	}
	  
	 
	
	
	  
}
