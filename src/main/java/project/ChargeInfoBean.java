package project;

public class ChargeInfoBean {
	
		 private int num;
		 private int user;
		 private int item;
		 private String code;
		 private int price;
		 private String date;
		 private String chamet;
		 private boolean  ccflag;
		 
		 
		 
		public int getNum() {
			return num;
		}
		public void setNum(int num) {
			this.num = num;
		}
		public int getUser() {
			return user;
		}
		public void setUser(int user) {
			this.user = user;
		}
		public int getItem() {
			return item;
		}
		public void setItem(int item) {
			this.item = item;
		}
		public String getCode() {
			return code;
		}
		public void setCode(String code) {
			this.code = code;
		}
		public int getPrice() {
			return price;
		}
		public void setPrice(int price) {
			this.price = price;
		}
		public String getDate() {
			return date;
		}
		public void setDate(String date) {
			this.date = date;
		}
		public String getChamet() {
			return chamet;
		}
		public void setChamet(String chamet) {
			this.chamet = chamet;
		}
		public boolean getCcflag() {
			return ccflag;
		}
		public void setCcflag(boolean ccflag) {
			this.ccflag = ccflag;
		}
		 
		 
		 
		 
}
